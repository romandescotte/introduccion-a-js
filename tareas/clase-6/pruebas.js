function pruebaObtenerMayorNumero(){
	console.assert(obtenerMayorNumero([1,2]) === 2, 'obtenerMayorNumero no obtuvo el mayor número');
}

pruebaObtenerMayorNumero();

function pruebaObtenerMenorNumero() {
	console.assert(obtenerMenorNumero([1,2]) === 1, 'obtenerMenorNumero no obtuvo el menor número');
}

pruebaObtenerMenorNumero();

